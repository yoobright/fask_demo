
def add_function(a, b, c=None):
    """
    sum = a + b + c
    :param a: int
    :param a: int
    :param c: int
    :return res: int
    """
    res = a + b +  c if c is not None else 0
    return res


def div_function(a, b):
    """
    res = a / b
    :param a: int
    :param a: int
    :return res: int
    """
    res = a / b
    return res